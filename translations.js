// Current language setting
let currentLanguage = 'ar';

// Function to change language
function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('karmaLanguage', lang);
    document.documentElement.lang = lang === 'it' ? 'it' : 'ar';
    document.documentElement.dir = lang === 'it' ? 'ltr' : 'rtl';
    translatePage();
}

// Function to translate a single string
function translate(str) {
    if (!str) return '';
    
    // Check if translation exists
    if (translations[str]) {
        return translations[str];
    }
    
    // Return original string if no translation
    return str;
}

// Function to translate page content
function translatePage() {
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        el.textContent = translate(key);
    });
    
    // Translate all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-placeholder');
        el.setAttribute('placeholder', translate(key));
    });
    
    // Translate all elements with data-i18n-title attribute
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-title');
        el.setAttribute('title', translate(key));
    });
    
    // Translate title
    document.querySelectorAll('title').forEach(function(el) {
        var key = el.getAttribute('data-i18n-title');
        if (key) {
            el.textContent = translate(key) + ' - Karma Restaurant';
        }
    });
    
    // Update navbar based on language
    updateNavbar();
}

// Function to get current language
function getCurrentLanguage() {
    return localStorage.getItem('karmaLanguage') || 'ar';
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    var savedLang = getCurrentLanguage();
    if (savedLang !== currentLanguage) {
        setLanguage(savedLang);
    }
});

